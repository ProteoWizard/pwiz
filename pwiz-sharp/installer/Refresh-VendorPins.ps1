<#
.SYNOPSIS
Regenerate src/Vendor/Common/VendorSdkPins.generated.cs from the vendor 7z archives
currently in the pwiz repo.

.DESCRIPTION
For each vendor, finds the most-recent commit that touched its archive (`git log -1
-- <path>`), constructs a raw.githubusercontent.com URL pinned to that commit, and
records the file's SHA-256. The output is a generated C# file containing a static
readonly array of `VendorSdkPin` records consumed by `VendorSdkLoader` at runtime.

Pins are self-contained:
  - The commit SHA is in the URL → GitHub serves byte-immutable content forever.
  - The SHA-256 is recorded → VendorSdkLoader rejects + re-downloads on mismatch.
  - Short SHA in the cache key → multiple pwiz-sharp versions keep their distinct
    vendor caches side-by-side.

.USAGE
Run as a pre-build step from the installer build (WiX project) so every installer
artifact pins to whichever vendor archive commits are current in HEAD. Day-to-day
dev builds use whatever .generated.cs is checked in.

    pwsh -File pwiz-sharp/installer/Refresh-VendorPins.ps1
#>

#requires -Version 7.0
$ErrorActionPreference = 'Stop'

$pwizRoot = (Resolve-Path "$PSScriptRoot/../..").Path
$outPath  = Join-Path $pwizRoot "pwiz-sharp/src/Vendor/Common/VendorSdkPins.generated.cs"
$repo     = "ProteoWizard/pwiz"

# (Name, RepoRelativeArchivePath, AssemblyPrefixes...) — the source of truth for
# vendor SDK pinning. Consumed by:
#   - VendorSdkPins.generated.cs (this script's output) — runtime resolver
#   - build.ps1's staging filter (dot-sourced) — strips vendor DLLs from the MSI
# Most archives live under pwiz_aux/msrc/utility/. Thermo on the pwiz-sharp side has
# its own newer .NET-8-compatible copy under pwiz-sharp/vendor-archives/.
$Script:Vendors = @(
    @{ Name = 'Thermo';   Path = 'pwiz-sharp/vendor-archives/vendor_api_Thermo.7z';
       Prefixes = @('ThermoFisher.', 'OpenMcdf') }
    @{ Name = 'Bruker';   Path = 'pwiz_aux/msrc/utility/vendor_api_Bruker.7z';
       Prefixes = @('Bruker.', 'BaseDataAccess', 'BDal.', 'CompassXtractMS',
                    'EDAL', 'Interop.EDAL', 'ProtocolBuffers', 'BaseError',
                    'BaseCommon', 'Compass', 'BaseTof', 'timsdata',
                    'baf2sql', 'mcd_lib') }
    @{ Name = 'Waters';   Path = 'pwiz_aux/msrc/utility/vendor_api_Waters.7z';
       Prefixes = @('MassLynxRaw', 'cdt', 'MassLynx') }
    @{ Name = 'Agilent';  Path = 'pwiz_aux/msrc/utility/vendor_api_Agilent.7z';
       Prefixes = @('Agilent.', 'BaseDataAccess', 'MIDAC.', 'Mhdac.',
                    'MassHunter.', 'MassSpecDataReader', 'agtsampleinforw') }
    @{ Name = 'ABI';      Path = 'pwiz_aux/msrc/utility/vendor_api_ABI.7z';
       Prefixes = @('Clearcore2.', 'Sciex.', 'SCIEX.', 'SciexToolKit', 'Interop.',
                    'OFX.Core.', 'ZipUtility', 'protobuf-net') }
    @{ Name = 'Shimadzu'; Path = 'pwiz_aux/msrc/utility/vendor_api_Shimadzu.7z';
       Prefixes = @('Shimadzu.', 'QTFL', 'GCMS', 'GCMSProto', 'QTFLProto',
                    'DataReader', 'IDQuantLSS', 'PeakIDEA',
                    'PeakItgLSS', 'MassLibrarySearch', 'MassCalcWrap',
                    'MassStandardSpectrum', 'DualProbeInterface',
                    'IoModule', 'LSSMng', 'CompressedTLM', 'CRHAKEI',
                    'CPCNLSS', 'CLFIO', 'LibDB', 'MSMSDB') }
    @{ Name = 'Mobilion'; Path = 'pwiz_aux/msrc/utility/vendor_api_Mobilion.7z';
       Prefixes = @('Mobilion.', 'MBISDK', 'MBI_SDK', 'MobilionShim') }
)

# When dot-sourced (`. Refresh-VendorPins.ps1`), exit before doing any work — caller
# only wants the $Vendors table.
if ($MyInvocation.InvocationName -eq '.') { return }

function Resolve-PinningCommit([string]$RelPath) {
    Push-Location $pwizRoot
    try {
        $sha = git log -1 --format=%H -- $RelPath
        if (-not $sha) { throw "no git history found for $RelPath" }
        return $sha.Trim()
    } finally { Pop-Location }
}

function Get-Sha256([string]$AbsPath) {
    (Get-FileHash -Path $AbsPath -Algorithm SHA256).Hash
}

# Format one vendor entry as a C# array initializer element. Keep the layout aligned
# with the runtime source for readable diffs.
function Format-PinCSharp($v, $commit, $sha256, $url) {
    $prefixCsv = ($v.Prefixes | ForEach-Object { "`"$_`"" }) -join ', '
    @"
    new VendorSdkPin(
        Name: "$($v.Name)",
        Version: "$($commit.Substring(0, 12))",
        Url: "$url",
        Sha256: "$sha256",
        AssemblyPrefixes: new[] { $prefixCsv }),
"@
}

$lines = @(
    "// <auto-generated>"
    "// Generated by pwiz-sharp/installer/Refresh-VendorPins.ps1."
    "// Do not edit by hand — rerun the script when a vendor SDK archive is updated."
    "// (No timestamp: git history is authoritative; embedding one would dirty the"
    "// file on every installer build and pollute diffs.)"
    "// </auto-generated>"
    ""
    "namespace Pwiz.Vendor.Common;"
    ""
    "/// <summary>Hardcoded vendor SDK pins baked in at installer-build time.</summary>"
    "internal static class VendorSdkPins"
    "{"
    "    /// <summary>The pin table consumed by <see cref=`"VendorSdkLoader`"/>."
    "    /// One entry per vendor; URL contains the commit SHA so the fetch is byte-immutable."
    "    /// </summary>"
    "    internal static readonly VendorSdkPin[] All ="
    "    {"
)

foreach ($v in $Vendors) {
    $abs = Join-Path $pwizRoot $v.Path
    if (-not (Test-Path $abs)) {
        Write-Warning "$($v.Path) not found, skipping"
        continue
    }
    $commit = Resolve-PinningCommit $v.Path
    $sha256 = Get-Sha256 $abs
    $url = "https://raw.githubusercontent.com/$repo/$commit/$($v.Path)"
    $lines += Format-PinCSharp $v $commit $sha256 $url
    Write-Host ("{0,-10}  {1}  {2}..." -f $v.Name, $commit.Substring(0, 12), $sha256.Substring(0, 16))
}

$lines += "    };"
$lines += "}"
$lines += ""

# Write only when content changed — keeps msbuild incrementality clean.
$newContent = ($lines -join "`r`n")
$prev = if (Test-Path $outPath) { Get-Content -Raw -Path $outPath } else { '' }
if ($newContent -ne $prev) {
    Set-Content -Path $outPath -Value $newContent -NoNewline -Encoding UTF8
    Write-Host "`nwrote $($outPath.Substring($pwizRoot.Length + 1))"
} else {
    Write-Host "`n(unchanged) $($outPath.Substring($pwizRoot.Length + 1))"
}
