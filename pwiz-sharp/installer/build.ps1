# ---------------------------------------------------------------------------
# Generate-FilesWxs: walk a staging directory and emit a WiX 4 source fragment
# with one <Component> per file. Component GUIDs are derived from the relative
# path via SHA-1 (RFC 4122 version-5 UUID shape) so MSI upgrade-detection sees
# the same component when the same file shows up across builds.
# ---------------------------------------------------------------------------
function Get-DeterministicGuid([string] $name) {
    $bytes = [System.Text.Encoding]::UTF8.GetBytes($name)
    $hash = [System.Security.Cryptography.SHA1]::HashData($bytes)
    $g = [byte[]]::new(16)
    [Array]::Copy($hash, 0, $g, 0, 16)
    # Stamp RFC 4122 version 5 (name-based, SHA-1) bits.
    $g[6] = [byte](($g[6] -band 0x0F) -bor 0x50)
    $g[8] = [byte](($g[8] -band 0x3F) -bor 0x80)
    return [System.Guid]::new($g)
}

function ConvertTo-WxsId([string] $s) {
    # WiX Ids: [A-Za-z_][A-Za-z0-9_.]*, max 72 chars. Substitute everything else with _
    # and prefix to guarantee starting char.
    $clean = ($s -replace '[^A-Za-z0-9_]', '_')
    if ($clean -match '^[0-9]') { $clean = "_$clean" }
    if ($clean.Length -gt 70) { $clean = $clean.Substring(0, 70) }
    return $clean
}

function Generate-FilesWxs {
    param(
        [Parameter(Mandatory)] [string] $StagingDir,
        [Parameter(Mandatory)] [string] $OutPath
    )

    $files = Get-ChildItem $StagingDir -Recurse -File | Sort-Object FullName

    # Build the set of unique relative directories so we can emit nested <Directory>
    # elements rooted at INSTALLFOLDER. "" = root.
    $dirSet = New-Object 'System.Collections.Generic.HashSet[string]' ([System.StringComparer]::OrdinalIgnoreCase)
    [void] $dirSet.Add('')
    foreach ($f in $files) {
        $relDir = [System.IO.Path]::GetDirectoryName(
            $f.FullName.Substring($StagingDir.Length + 1))
        if ($null -eq $relDir) { $relDir = '' }
        $relDir = $relDir.Replace('\', '/')
        # Add every ancestor too (e.g. runtimes/win-x64 → also add runtimes).
        $parts = if ($relDir -eq '') { @() } else { $relDir -split '/' }
        for ($i = 0; $i -lt $parts.Length; $i++) {
            $ancestor = ($parts[0..$i] -join '/')
            [void] $dirSet.Add($ancestor)
        }
    }

    function DirId([string] $relDir) {
        if ($relDir -eq '') { return 'INSTALLFOLDER' }
        return 'd_' + (ConvertTo-WxsId $relDir)
    }

    $sb = [System.Text.StringBuilder]::new()
    [void] $sb.AppendLine('<?xml version="1.0" encoding="UTF-8"?>')
    [void] $sb.AppendLine('<!-- AUTO-GENERATED by installer/build.ps1 — do not edit by hand. -->')
    [void] $sb.AppendLine('<Wix xmlns="http://wixtoolset.org/schemas/v4/wxs">')
    [void] $sb.AppendLine('  <Fragment>')

    # Directory tree. Sort by depth so parents come before children when nested
    # in the emitted XML; the WiX compiler doesn't care about ordering, but it
    # reads better.
    $sortedDirs = $dirSet | Sort-Object @{ Expression = { ($_ -split '/').Length } }, @{ Expression = { $_ } }

    # We emit a single DirectoryRef Id="INSTALLFOLDER" with nested Directory
    # elements. Build a parent → children map first.
    $childrenByParent = @{}
    foreach ($d in $sortedDirs) {
        if ($d -eq '') { continue }
        $parent = [System.IO.Path]::GetDirectoryName($d.Replace('/', '\'))
        $parent = if ($null -eq $parent) { '' } else { $parent.Replace('\', '/') }
        if (-not $childrenByParent.ContainsKey($parent)) { $childrenByParent[$parent] = @() }
        $childrenByParent[$parent] += $d
    }

    function EmitDir([string] $relDir, [int] $indent) {
        $pad = '  ' * $indent
        if ($relDir -eq '') {
            [void] $sb.AppendLine("$pad<DirectoryRef Id=`"INSTALLFOLDER`">")
        } else {
            $name = Split-Path -Leaf $relDir
            $id = DirId $relDir
            $hasChildren = $childrenByParent.ContainsKey($relDir)
            if ($hasChildren) {
                [void] $sb.AppendLine("$pad<Directory Id=`"$id`" Name=`"$name`">")
            } else {
                [void] $sb.AppendLine("$pad<Directory Id=`"$id`" Name=`"$name`" />")
                return
            }
        }
        if ($childrenByParent.ContainsKey($relDir)) {
            foreach ($child in $childrenByParent[$relDir] | Sort-Object) {
                EmitDir $child ($indent + 1)
            }
        }
        if ($relDir -eq '') {
            [void] $sb.AppendLine("$pad</DirectoryRef>")
        } else {
            [void] $sb.AppendLine("$pad</Directory>")
        }
    }
    EmitDir '' 2

    # Component group with one Component per file. WiX uses each Component's
    # Directory= attribute to route it into the right installed location.
    [void] $sb.AppendLine('    <ComponentGroup Id="StagedFiles">')
    foreach ($f in $files) {
        $rel = $f.FullName.Substring($StagingDir.Length + 1)
        $relDir = [System.IO.Path]::GetDirectoryName($rel)
        if ($null -eq $relDir) { $relDir = '' } else { $relDir = $relDir.Replace('\', '/') }
        $dirId = DirId $relDir
        $compId = 'c_' + (ConvertTo-WxsId $rel)
        $fileId = 'f_' + (ConvertTo-WxsId $rel)
        $guid = (Get-DeterministicGuid $rel).ToString().ToUpper()
        # XML-escape filename and path attribute values (just &, <, >, ", ').
        $absPath = ($f.FullName -replace '&', '&amp;') -replace '"', '&quot;'
        $name    = ($f.Name      -replace '&', '&amp;') -replace '"', '&quot;'
        [void] $sb.AppendLine("      <Component Id=`"$compId`" Guid=`"{$guid}`" Directory=`"$dirId`">")
        [void] $sb.AppendLine("        <File Id=`"$fileId`" Source=`"$absPath`" Name=`"$name`" />")
        [void] $sb.AppendLine("      </Component>")
    }
    [void] $sb.AppendLine('    </ComponentGroup>')

    [void] $sb.AppendLine('  </Fragment>')
    [void] $sb.AppendLine('</Wix>')

    [System.IO.File]::WriteAllText($OutPath, $sb.ToString(), [System.Text.Encoding]::UTF8)
    Write-Host "    wrote $([System.IO.Path]::GetFileName($OutPath)) ($($files.Count) components)"
}
# ---------------------------------------------------------------------------

$installerDir = $PSScriptRoot
$pwizSharp    = (Resolve-Path "$installerDir/..").Path
$msconvertGui = Join-Path $pwizSharp "src/MsConvertGUI/MsConvertGUI.csproj"
$buildOutput  = Join-Path $pwizSharp "src/MsConvertGUI/bin/Release/net8.0-windows"
$outDir       = Join-Path $installerDir "build"
$stagingDir   = Join-Path $outDir "stage"   # filtered copy of $buildOutput
$scope        = if ($PerMachine) { "perMachine" } else { "perUser" }
$msiName      = if ($PerMachine) { "ProteoWizard-Sharp-perMachine.msi" } else { "ProteoWizard-Sharp.msi" }
$msiPath      = Join-Path $outDir $msiName

