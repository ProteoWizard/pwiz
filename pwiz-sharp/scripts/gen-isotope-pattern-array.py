"""Generate IsotopePatternRange.g.cs from cpp's IsotopePatternRange.ipp.

The cpp file is a static C++ array literal of (mass, MzRange[9]) tuples. We
parse it once and emit a C# `static readonly IsotopePatternEntry[]` with the
same numeric values. Re-run when cpp updates the array (the script is a
build-time convenience, not used at runtime).
"""

from pathlib import Path
import re

SRC = Path(r"C:\dev\pwiz-msconvert-pr\pwiz\analysis\dia_umpire\IsotopePatternRange.ipp")
OUT = Path(r"C:\dev\pwiz-msconvert-pr\pwiz-sharp\src\Analysis\DiaUmpire\IsotopePatternRange.g.cs")

text = SRC.read_text()

# Entry pattern: { <mass>, { {b0,e0}, {b1,e1}, ... } },
entry_re = re.compile(
    r"\{\s*(\d+)\s*,\s*\{\s*((?:\{[^{}]*\}\s*,?\s*)+)\}\s*\}",
    re.DOTALL,
)
pair_re = re.compile(r"\{\s*([\d.e+\-]+)f\s*,\s*([\d.e+\-]+)f\s*\}")

entries = []
for m in entry_re.finditer(text):
    mass = int(m.group(1))
    pairs = pair_re.findall(m.group(2))
    if len(pairs) != 9:
        raise SystemExit(f"mass {mass}: expected 9 pairs, got {len(pairs)}")
    entries.append((mass, pairs))

assert len(entries) == 166, f"expected 166 entries, got {len(entries)}"

out_lines = [
    "// <auto-generated/> by scripts/gen-isotope-pattern-array.py from cpp IsotopePatternRange.ipp.",
    "// Do not edit by hand; re-run the script when the cpp .ipp file changes.",
    "namespace Pwiz.Analysis.DiaUmpire;",
    "",
    "internal static class IsotopePatternRange",
    "{",
    "    /// <summary>One entry per mass anchor: mass (Da) + per-isotope MzRange (9 isotopes).</summary>",
    "    public readonly struct Entry",
    "    {",
    "        /// <summary>Anchor mass in daltons.</summary>",
    "        public int Mass { get; }",
    "        /// <summary>Per-isotope mz ranges, length 9. Index 0 is mono.</summary>",
    "        public MzRange[] Ranges { get; }",
    "        /// <summary>Creates an entry with the given mass and 9 isotope ranges.</summary>",
    "        public Entry(int mass, MzRange[] ranges) { Mass = mass; Ranges = ranges; }",
    "    }",
    "",
    "    /// <summary>166-entry lookup table from cpp IsotopePatternRange.ipp.</summary>",
    "    public static readonly Entry[] Table = new Entry[]",
    "    {",
]

for mass, pairs in entries:
    range_strs = ", ".join(f"new MzRange({b}f, {e}f)" for b, e in pairs)
    out_lines.append(f"        new Entry({mass}, new MzRange[] {{ {range_strs} }}),")

out_lines.extend([
    "    };",
    "}",
    "",
])

OUT.write_text("\n".join(out_lines))
print(f"wrote {OUT} ({len(entries)} entries)")
